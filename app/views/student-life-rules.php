        <h1>삼경인문고등학교 학교생활규정</h1>
        <p>제정 : 2026. 07. 05.<br>개정 : 2026. 07. 05.</p>
    </header>

    <div class="school-rules-body">
        <section class="school-rule-chapter">
            <h2>제 1장 총 칙</h2>
            <article>
                <h3>제1조(명칭)</h3>
                <p>이 규정은 ‘삼경인문고등학교 학교생활규정’이라 한다.</p>
            </article>
            <article>
                <h3>제2조(목적)</h3>
                <p>이 규정은 삼경인문고등학교 학생들의 교내외 생활 전반에 관한 제반 사항을 명확히 규정함을 목적으로 한다. 이를 통해 학생들로 하여금 본교의 건학 이념인 ‘삼경(경천 敬天, 경인 敬人, 경물 敬物)’의 정신을 일상에서 실천하고, 존중, 배려, 책임의 가치를 내면화하여, 타의 모범이 되는 철저한 예절과 준법의식을 갖춘 차세대 엘리트로 성장하게 함을 궁극적인 목표로 한다.</p>
            </article>
        </section>

        <section class="school-rule-chapter">
            <h2>제 2장 언행 및 보안</h2>
            <article>
                <h3>제3조(보안 및 비속어)</h3>
                <p>본교의 대화나 관련 업무는 외부로 절대 유출하지 아니하며, 단체 대화 방 내 학생 신분에 맞지 않는 비속어나 수위 발언은 엄격히 금지한다.</p>
            </article>
            <article>
                <h3>제4조(비속어 기준)</h3>
                <p>비속어는 네이버 국어사전 기준으로 뜻풀이에 ‘속되게’가 포함되는 말을 기준으로 한다.</p>
            </article>
            <article>
                <h3>제5조(경어 및 압존법)</h3>
                <p>모든 대화는 경어체를 사용하며 종결 어미는 ‘-다’, ‘-까’로 끝내는 것을 원칙으로 한다. 명령형 경어 및 경어를 줄여 쓰는 것은 금지하며, 상급자 간의 대화 시 공대를 줄이는 압존법(예: 000선배님, 000선배가 그랬습니다)을 반드시 지켜야 한다.</p>
            </article>
        </section>

        <section class="school-rule-chapter">
            <h2>제 3장 인사 및 예절</h2>
            <article>
                <h3>제6조(인사)</h3>
                <p>본교의 학생들은 타인을 공경하는 ‘경인(敬人)’의 정신과 예절 캠페인(존중, 배려, 책임)에 입각하여, 교내 모든 대화방에 출입할 때 정해진 양식에 따라 예의를 갖춰 인사해야 한다.</p>
            </article>
            <article>
                <h3>제7조(출입 인사 및 인사 텀)</h3>
                <p>채팅방의 성격에 따른 출입 인사 양식과 재인사 기준 시간(이하 텀)은 다음 각 호와 같이 규정한다. (※ 기준 시간 내 대화가 이어질 경우 추가 인사는 생략한다.)</p>
                <ol>
                    <li>
                        단체 대화방 (단대)
                        <ol>
                            <li>양식: "삼경의 이름으로 O학년 OOO입니다."</li>
                            <li>인사 텀: 본인 마지막 발언 기준 10분</li>
                        </ol>
                    </li>
                    <li>
                        개인 인사 (직속방 및 개인 채팅방 포함)
                        <ol>
                            <li>양식: "삼경, 000 선배님 O학년 OOO입니다."</li>
                            <li>인사 텀: 선배의 마지막 발언 기준 10분</li>
                        </ol>
                    </li>
                    <li>
                        소속 관 / 직속 대화방
                        <ol>
                            <li>양식: "삼경, (소속 관 이름)관 선배님들 O학년 OOO입니다."</li>
                            <li>인사 텀: 본인 마지막 발언 기준 10분</li>
                        </ol>
                    </li>
                    <li>
                        선배 개인 채팅방 (출입 요청)
                        <ol>
                            <li>양식: "삼경, (소속 관 이름)관 O학년 OOO입니다. 출입해도 되겠습니까?"</li>
                            <li>인사 텀: 마지막 발언 기준 3시간</li>
                        </ol>
                    </li>
                </ol>
            </article>
            <article>
                <h3>제8조(선배 부재 시 예외 규정)</h3>
                <p>제7조 ④항에 따라 선배의 개인 채팅방에 출입 인사를 남겼으나 5분 이상 응답이 없는 경우, 긴급한 사안에 한하여 다음의 양식으로 사유를 남긴 후 본론을 전달할 수 있다.</p>
                <p>양식: "(사유)한 용건으로 급히 출입하였습니다. 양해 부탁드립니다."</p>
            </article>
        </section>

        <section class="school-rule-chapter">
            <h2>제 4장 관등 및 대답</h2>
            <article>
                <h3>제9조(관등)</h3>
                <p>관등은 누군가 자신을 지칭했을 때 자신의 학년과 성명을 밝히는 행위이다. (양식: "예 선배님, O학년 OOO입니다." 관등 텀 : 없음. 출입인사 이후 해당되는 관등만)</p>
                <ol>
                    <li>
                        관등은 다음 각 호의 어느 하나에 해당되는 경우 시행한다.
                        <ol>
                            <li>'야', '너' 등 본인에 해당되는 지칭을 받았을 때</li>
                            <li>별명을 제외한 정확한 본인 이름이 언급되었을 때</li>
                            <li>거수 또는 멘션이 포함될 때 (예: 밥 먹은 사람 거수)</li>
                            <li>본인이 포함되는 그룹을 지칭하는 경우 (예: 경물관 전원)</li>
                        </ol>
                    </li>
                </ol>
            </article>
            <article>
                <h3>제10조(대답 및 순서)</h3>
                <p>기본 대답은 “예/아닙니다/시정하겠습니다 선배님 (할 말)”의 양식을 준수하며, “예” 단독 사용 및 도치법은 금지한다.</p>
                <ol>
                    <li>기본 대화 순서: 출입 인사 → 개인 인사 → 관등 → 대답 → 할 말</li>
                </ol>
            </article>
            <article>
                <h3>제11조(시정 절차)</h3>
                <p>교칙 미흡 사항 발생 시, 본교 재학생은 시정 멘트를 남겨야 한다.</p>
                <ol>
                    <li>순서: 출입 인사 → 개인 인사 → 관등 → 시정 → 대답 → 할 말</li>
                    <li>예외(타이밍 미흡): 선배와 후배 간 네트워크 지연 등 타이밍 차이로 미흡이 발생한 경우, 답장 기능으로 ‘타이밍입니다’를 작성하여 상급자가 확인하면 벌점을 면제한다.</li>
                </ol>
            </article>
        </section>

        <section class="school-rule-chapter">
            <h2>제 5장 상벌점 및 징계</h2>
            <article>
                <h3>제12조(벌점)</h3>
                <ol>
                    <li>1점: 메시지 삭제(혼란을 줄 만한 공지사항의 오류 및 개인정보 관련 사항은 제외), 인튀(인사 후 바로 퇴장), 읽씹(읽고 무시함), 무단결석</li>
                    <li>2점: 인사 미흡, 관등 미흡, 대답 미흡, 현활(현재 활동) 중 5분 내 미대답 (예절 기간)</li>
                    <li>3점: 호칭/경어 미흡, 일지 미작성, 무단불참</li>
                    <li>4점: 비속어 사용, 지시 불이행, 단체 대화방 내 싸움, 보안 규정 위반, 잠수 중 읽음</li>
                    <li>1~5점: 선배 재량</li>
                </ol>
            </article>
            <article>
                <h3>제13조(상점)</h3>
                <ol>
                    <li>1점: 일지 작성, 회의록 작성, 자유 게시판 작성, 갈매기 홍보 상점, 출석 4회 이상, 근무 상점(삼경원)</li>
                    <li>2점: 교내 자치 활동 (하루 1회 제한), 한자 깜지 200회(한글 포함), 삼경인 선서문 3회 작성 (하루 1회 제한), 예절 테스트, 문장 깜지(직속 선배에게 문장 받아서 시행)</li>
                    <li>3점: 후배 교육 자료 작성 (하루 1회 제한)</li>
                    <li>4점: 추가할 규정이 있다면 이곳에 작성</li>
                    <li>1~5점: 선배 재량</li>
                </ol>
            </article>
            <article>
                <h3>제14조(상점 증빙 및 제출 절차)</h3>
                <ol>
                    <li>모든 상점 제출물은 성의 있게 작성되어야 하며, 삼경원 과반수 동의 시 기각될 수 있다.</li>
                    <li>상점 제출은 삼경원 개인 채팅방으로 하며, 사진 첨부를 통해 얻는 모든 상점은 상단 또는 하단에 "2026.OO.OO (관명) O학년 OOO"을 작성하고 그 위에 형광펜으로 표시를 해야 상점으로 인정된다.</li>
                    <li>상점 제출 시, [(관명) O학년 OOO, (상점 사유) + O점]의 양식을 갖춰야 한다.</li>
                    <li>필사가 필요한 모든 제출물은 종이 위에서 작성되어야 한다. 태블릿, 노트북을 비롯한 전자기기 사용을 금지한다.</li>
                </ol>
            </article>
        </section>
