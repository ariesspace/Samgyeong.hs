    <h1>상벌점 리스트</h1>
    <p class="muted">삼경인의 생활 태도와 공동체 기여를 기록하는 상점과 벌점 운영 기준입니다.</p>

    <section class="points-guide-summary">
        <article>
            <span>상점</span>
            <h2>공동체를 더 좋게 만든 행동</h2>
            <p>자발적인 봉사, 예절 실천, 행사 지원, 모범적인 생활 태도 등을 기록합니다.</p>
        </article>
        <article>
            <span>벌점</span>
            <h2>함께 지키기로 한 약속 위반</h2>
            <p>생활 규정 위반, 무단 누락, 공동체 질서 훼손 등 개선이 필요한 행동을 기록합니다.</p>
        </article>
        <article>
            <span>확인</span>
            <h2>내 기록은 마이페이지에서</h2>
            <p>본인에게 부여된 상벌점 내역은 마이페이지의 상벌점 현황에서 확인할 수 있습니다.</p>
        </article>
    </section>

