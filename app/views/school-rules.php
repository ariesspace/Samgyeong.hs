    <header class="school-rules-title">
        <h1>삼경인문고등학교 학칙</h1>
        <p>제정 : 2026. 07. 01.<br>개정 : 2026. 07. 02.</p>
    </header>

    <div class="school-rules-body">
        <section class="school-rule-chapter">
            <h2>제 1장 총 칙</h2>
            <article>
                <h3>제1조(목적)</h3>
                <p>본 고등학교는 전통 명문 사립학교로서 엘리트를 양성하며, ‘삼경(경천 敬天, 경인 敬人, 경물 敬物)’의 정신을 바탕으로 철저한 예절 교육과 끈끈한 공동체 의식을 함양함을 목적으로 한다.</p>
            </article>
            <article>
                <h3>제2조(명칭)</h3>
                <p>본교는 ‘삼경인문고등학교’라 한다.</p>
            </article>
        </section>

        <section class="school-rule-chapter">
            <h2>제 2장 수업연한 및 입학자격</h2>
            <article>
                <h3>제3조(수업연한)</h3>
                <p>본교의 수업연한은 3년으로 한다.</p>
            </article>
            <article>
                <h3>제4조(입학자격)</h3>
                <p>본교 제1학년에 입학할 수 있는 자는 다음 각 호의 1에 해당하는 자로 한다.</p>
                <ol>
                    <li>직속팸 및 군대팸 경험이 있는자. (스터디팸 및 일지팸 경험은 제외함)</li>
                    <li>삼경인문고에 성실히 참여 가능한자</li>
                    <li>쉽게 포기하지 않는자</li>
                </ol>
            </article>
        </section>

        <section class="school-rule-chapter">
            <h2>제 3장 직속편제 및 학생 정원</h2>
            <article>
                <h3>제5조(직속)</h3>
                <p>본교의 학생들은 삼경의 정신에 따라 세 개의 관(기숙사 및 직속) 중 하나에 소속된다.</p>
                <ol>
                    <li>경천관(敬天館) - 하늘</li>
                    <li>경인관(敬人館) - 사람</li>
                    <li>경물관(敬物館) - 만물</li>
                </ol>
                <p>단, 각 관은 3학년 중 ‘관장’을 두며, 2학년 중 ‘부관장’을 둔다. 인원에 공백이 있을 시 각 관 내에서 3학년의 관리하에 임시로 임명이 가능하다.</p>
            </article>
            <article>
                <h3>제6조(학생정원)</h3>
                <p>본교의 학생정원은 최대 학년 당 8인으로 한다.</p>
            </article>
        </section>

        <section class="school-rule-chapter">
            <h2>제 4장 출석 및 시험</h2>
            <article>
                <h3>제7조(출석)</h3>
                <p>본 제도는 타인을 공경하는 ‘경인(敬人)’의 정신을 바탕으로, 단체 대화방(이하 단대) 내 활발하고 따뜻한 교류를 위해 일일 출석체크를 진행한다. 재학생은 다음 각 호의 규정을 준수하여 출석을 인증해야 한다.</p>
                <ol>
                    <li>
                        출석 절차: 꼬리물기 릴레이 형식으로 다음 각 호의 절차를 캡처하여야 한다.
                        <ol>
                            <li>응답 (존중): 직전 출석자가 남긴 질문에 대해 성의 있게 대답한다.</li>
                            <li>질문 (배려): 다음 출석자를 위해 가벼운 안부나 일상적인 질문을 새롭게 남긴다.</li>
                        </ol>
                    </li>
                    <li>
                        출석 유의사항
                        <ol>
                            <li>자음(예: 'ㅋ', 'ㅎ')이나 이모티콘만으로 이루어진 메시지는 인정하지 않는다.</li>
                            <li>캡처 시간과 댓글 작성 시간의 차이가 5분을 초과할 경우 출석으로 인정하지 않는다.</li>
                        </ol>
                    </li>
                </ol>
            </article>
            <article>
                <h3>제8조(출석 제출 양식 및 시간대)</h3>
                <p>출석 인증 댓글은 <strong>[(소속 관 이름) O학년 OOO, (출석 시간대) 출석합니다.]</strong> 의 양식을 반드시 갖추어야 하며, 시간대 기준은 다음 각 호와 같다.</p>
                <ol>
                    <li>새벽: 00:00 ~ 04:59</li>
                    <li>오전: 05:00 ~ 10:59</li>
                    <li>점심: 11:00 ~ 14:59</li>
                    <li>오후: 15:00 ~ 17:59</li>
                    <li>저녁: 18:00 ~ 22:59</li>
                </ol>
            </article>
            <article>
                <h3>제9조(출석 관련 상벌점)</h3>
                <ol>
                    <li>벌점: 기준 기간 내 2회 미만 출석 시 벌점 1점을 부여한다.</li>
                    <li>상점: 기준 기간 내 4회 이상 출석 시 상점 1점을 부여한다.</li>
                    <li>징계: 연속 3일 동안 무단결석 시 퇴학 조치를 진행한다.</li>
                </ol>
            </article>
            <article>
                <h3>제10조(월간 시험 및 관 이동)</h3>
                <p>본교 재학생은 매월 소속 관(기숙사)이 변경되기 전, 학업 및 소양 점검을 위해 월 1회 의무적으로 시험을 치러야 한다.</p>
                <ol>
                    <li>시험 주관: 해당 시험 자료는 학생의 직속 선배가 직접 제작하여 후배를 대상으로 실시한다.</li>
                    <li>결과 보고: 시험이 종료된 후, 직속 선배는 그 결과를 각 관의 '관장'에게 보고하여야 한다.</li>
                </ol>
            </article>
        </section>

        <section class="school-rule-chapter">
            <h2>제 5장 학생 자치 기구</h2>
            <article>
                <h3>제11조(삼경원)</h3>
                <p>본교는 삼경(三敬)의 정신과 오랜 학교의 전통을 계승하고, 학생 스스로 엄격한 규율을 세우기 위하여 최고 학생 자치기구인 ‘삼경원(三敬院)’을 둔다. 삼경원은 단순한 학생회를 넘어 교내 자치 활동의 중추적인 역할을 수행하며, 다음 각 호의 핵심 가치를 지향한다.</p>
                <ol>
                    <li>규율 : 공동체가 함께 지키는 철저한 질서 확립</li>
                    <li>계승 : 삼경의 이념과 명문 사립고로서의 학교 전통 유지</li>
                    <li>대표 : 학생의 의견을 하나로 모아 학교와 연결하는 소통 창구</li>
                </ol>
            </article>
            <article>
                <h3>제12조(주요 직무)</h3>
                <p>삼경원은 교내 질서 유지와 올바른 학풍 조성을 위해 다음 각 호의 주요 직무를 주관한다.</p>
                <ol>
                    <li>1학년 신규인원 대상 예절 및 전통 교육 주관</li>
                    <li>교내 생활규정 위반자 선도 및 정독 지도</li>
                    <li>학생 민원 수렴 및 처리 방안 마련</li>
                    <li>학생 공동체 의식 함양을 위한 자치 행사 기획</li>
                </ol>
            </article>
        </section>
