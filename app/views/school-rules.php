    <header class="school-rules-title">
        <h1>삼경인문고등학교 운영 및 생활 규정</h1>
        <p>2026. 07. 초안</p>
    </header>

    <div class="school-rules-body">
        <section class="school-rule-chapter">
            <h2>제1장 총칙</h2>
            <article>
                <h3>제1조 (목적)</h3>
                <p>본 고등학교는 전통 명문 사립학교로서 엘리트를 양성하며, ‘삼경(경천 敬天, 경인 敬人, 경물 敬物)’의 정신을 바탕으로 철저한 예절 교육과 끈끈한 공동체 의식을 함양함을 목적으로 한다.</p>
            </article>
            <article>
                <h3>제2조 (학생 조직 및 학생회)</h3>
                <p>본 고등학교는 학교 전통을 이끄는 최고 자치기구로서 학생회 ‘삼경원(三敬院)’을 운영한다. 삼경원은 교내 정독 및 합산, 신입생 교육 및 민원 해결의 중추적인 역할을 담당한다.</p>
            </article>
            <article>
                <h3>제3조 (직속 및 기숙사 체제)</h3>
                <p>본교의 학생들은 삼경의 정신에 따라 세 개의 관(기숙사 및 직속) 중 하나에 소속된다.</p>
                <ul>
                    <li>경천관(敬天館) - 하늘</li>
                    <li>경인관(敬人館) - 사람</li>
                    <li>경물관(敬物館) - 만물</li>
                </ul>
                <p>각 관은 3학년 중 ‘관장’을 두며, 2학년 중 ‘부관장’을 둔다. 인원에 공백이 있을 시 각 관 내에서 3학년의 관리하에 임시로 임명이 가능하다.</p>
            </article>
        </section>

        <section class="school-rule-chapter">
            <h2>제2장 언행 및 보안</h2>
            <article>
                <h3>제4조 (보안 및 비속어)</h3>
                <p>본교의 대화나 관련 업무는 외부로 절대 유출하지 아니하며, 학생 신분에 맞지 않는 비속어나 수위 발언은 엄격히 금지한다.</p>
            </article>
            <article>
                <h3>제5조 (경어 및 압존법)</h3>
                <p>모든 대화는 경어체를 사용하며 종결 어미는 ‘-다’, ‘-까’로 끝내는 것을 원칙으로 한다.</p>
                <p>명령형 경어 및 경어를 줄여 쓰는 것은 금지하며, 상급자 간의 대화 시 공대를 줄이는 압존법(예: 000선배님, 000선배가 그랬습니다)을 반드시 지켜야 한다.</p>
            </article>
        </section>

        <section class="school-rule-chapter">
            <h2>제3장 인사 및 대답</h2>
            <article>
                <h3>제6조 (출입 인사 및 개인 인사)</h3>
                <div class="rule-example-grid">
                    <div>
                        <strong>단대 (단체 대화방)</strong>
                        <p><b>출입 인사</b> 삼경의 이름으로 0학년 000입니다.</p>
                        <p><b>인사 텀</b> 본인 마지막 말 기준 10분</p>
                    </div>
                    <div>
                        <strong>개인 인사</strong>
                        <p><b>양식</b> 삼경, 000선배님 0학년 000입니다. (선배 이름 나열 가능)</p>
                        <p><b>인사 텀</b> 선배 마지막 말 기준 10분 (직속방 및 개인 채팅방 동일 적용)</p>
                    </div>
                    <div>
                        <strong>관 / 직속방 출입 인사</strong>
                        <p><b>양식</b> 삼경, (소속 관 이름)관 0학년 000입니다.</p>
                        <p><b>인사 텀</b> 본인 마지막 말 기준 10분</p>
                    </div>
                    <div>
                        <strong>갠 (개인 채팅방) 출입 인사</strong>
                        <p><b>양식</b> 삼경, (소속 관 이름)관 0학년 000입니다. 출입해도 되겠습니까?</p>
                        <p><b>인사 텀</b> 본인 마지막 말 기준 3시간</p>
                    </div>
                </div>
            </article>
            <article>
                <h3>제7조 (관등)</h3>
                <p>관등은 누군가 자신을 지칭했을 때 자신의 학년과 성명을 밝히는 행위이다.</p>
                <ul>
                    <li><b>양식</b> 예, 0학년 000입니다.</li>
                    <li><b>적용 대상</b> 별명을 제외한 ‘야’, ‘너’ 등 본인에 해당되는 지칭이 나올 경우 전부 관등을 실시해야 한다.</li>
                </ul>
            </article>
            <article>
                <h3>제8조 (대답 및 순서)</h3>
                <p>기본 대답은 “예 선배님”, “아닙니다 선배님”의 양식을 지키며, 긍정 대답 시 “예 그렇습니다”, 부정 대답 시 “시정하겠습니다” 등으로 명확히 답한다. “예” 단독 사용 및 도치법은 금지한다.</p>
                <p class="rule-order">기본 대화 순서 : 출입 인사 → 개인 인사 → 관등 → 대답 → 할 말</p>
            </article>
        </section>

        <section class="school-rule-chapter">
            <h2>제4장 시정</h2>
            <article>
                <h3>제9조 (시정 절차)</h3>
                <p>교칙 미흡 사항 발생 시, 본교 재학생은 시정 멘트를 남겨야 한다.</p>
                <p class="rule-order">순서 : 출입 인사 → 개인 인사 → 관등 → 시정 → 대답 → 할 말</p>
                <p><b class="rule-alert">[예외 - 타이밍 미흡]</b><br>선배와 후배 간 네트워크 지연 등 타이밍 차이로 미흡이 발생한 경우, 답장 기능으로 ‘타이밍입니다’를 작성하여 상급자가 확인하면 벌점을 면제한다.</p>
            </article>
        </section>
